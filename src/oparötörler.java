public class oparötörler {
    public static void main(String[] args) {
        int naber = 10;
        int güzel = 92;
        System.out.println("Toplama: "+  (naber+güzel));
        System.out.println("Cıkarma: "+ (güzel-naber));
        System.out.println("Carpma: "+ (güzel*naber));
        System.out.println("Bölme: "+ (güzel/naber));
        System.out.println("Bölme: "+ ((double)güzel/naber));
        System.out.println("Bölme: "+ (güzel/(double)naber));

        int m=9;
        m++;
        System.out.println("M'nın yeni degeri: " + m);

        int t =99;
        t--;
        System.out.println("T'nin yeni degeri: " + t);

        int y = 19;
        System.out.println("Y'nin degeri: " + y);
        y += 3;
        System.out.println("Yeni degeri: " + y);


    }
}
